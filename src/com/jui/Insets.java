package com.jui;

/**
 * Insets
 *
 * @author Ian
 * @version 1.0
 */
public class Insets {
    public final int left;
    public final int top;
    public final int right;
    public final int bottom;

    public Insets(int left, int top, int right, int bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
